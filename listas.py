lista = [1, 3, 12, 8, 2]

# append

print(lista)

lista.append(3)

print(lista)

# insert

lista.insert(2, 10)
print(lista)

# extend

lista2 = [1, 2, 3]

lista.extend(lista2)

print(lista)

# pop

lista.pop(0)
print(lista)

# remove

lista.remove(3)
print(lista)

# count

lista.count(2)
print(lista, lista.count(2))

# sort

lista.sort()
print(lista)

# index

print(lista.index(10))

