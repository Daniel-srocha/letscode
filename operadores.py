# operações matemáticas

"""
    - Soma: +
    - Subtração: -
    - Multiplicação: *
    - Divisão: /
    - Divisão inteira: //
    - Resto da divisão: %
    - Potência: **

"""

n1 = 10
n2 = 20 

print(n1 + n2)
print(n1 - n2)
print(n1 * n2)
print(n1 / n2)
print(n1 // n2)
print(n1 % n2)
print(n1 ** n2)




